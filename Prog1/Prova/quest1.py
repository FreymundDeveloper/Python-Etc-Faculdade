# Um barzinho deseja realizar o cadastro de pedidos de uma mesa. Existe uma classe base denominada Itens para
# representar os itens consumidos na mesa do bar. Essa classe deve conter a quantidade de itens vendidos na mesa,
# nome de cada item vendido, preço de cada item, e total da despesa dos itens. Existe também uma classe derivada
# denominada Mesa que deve conter o número da mesa e o nome do cliente. O programa principal, por sua vez,
# contém a alocação de um objeto da classe Mesa, chama o método que lê os dados a respeito da Mesa, calcula a despesa
# total dos itens consumidos e imprime o resultado final do fechamento da mesa.

# Descrição da classe base Itens: Dados privados: 01) possui instâncias para guardar as seguintes características da
# classe: quantidade de itens vendidos na mesa, Nome de cada item vendido na mesa, preço de cada item, e total da
# despesa dos itens.

# Métodos públicos:
# 02) possui um construtor sem parâmetros de entrada para iniciar os dados da classe com valores default;
# 03) possui um método para ler os dados da classe Itens;
# 04) possui um método para calcular a despesa dos itens;
# 05) possui um método para imprimir em uma String os dados da classe Itens.

# Descrição da classe derivada Mesa:
# Dados privados:
# 06) possui instâncias para guardar o número da mesa e nome do cliente.

# Métodos públicos: 07) possui um construtor sem parâmetros de entrada para iniciar as variáveis de instância com
# valores default; 08) possui um método para ler os dados referentes à classe Mesa juntamente com os dados referentes
# ao consumo contido na classe Itens; 09) possui um método para imprimir em uma string os dados da classe Mesa além
# dos dados da classe Itens.

# Descrição do programa principal: 10) possui uma instrução que declara um objeto da classe Mesa; 11) possui uma
# instrução que chama o método que realiza a leitura dos dados da classe Mesa além dos demais dados da classe Itens;
# 12) possui uma instrução que chama o método que calcula a despesa dos itens consumidos na mesa; 13) possui uma
# instrução que chama o método que imprime em uma string os dados da classe Mesa, juntamente com os demais dados da
# classe Itens.
