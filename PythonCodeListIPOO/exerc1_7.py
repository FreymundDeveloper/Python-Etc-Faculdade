class Pessoa:
    nome = None
    idd = None
    peso = None
    alt = None

    def __init__(self, nome, idd, peso, alt):
        self.nome = nome
        self.idd = idd
        self.peso = peso
        self.alt = alt

    def env(self, idd):
        self.idd = idd

    def eng(self, idd):
        self.peso += self.idd * 0.5
        return self.peso

    def cres(self, idd):
        if self.idd < 21:
            self.alt += self.idd * 0.5
            return self.alt
        else:
            return self.alt


nome = input('Digite seu nome:')
idd = float(input('Digite sua idade:'))
peso = float(input('Digite o seu peso:'))
alt = float(input('Digite a sua altura:'))
p = Pessoa(nome, idd, peso, alt)
p.env(float(input('Quantos anos quer envelhecer:')))
print(str('Peso:'), p.eng(float))
print(str('Altura:'), p.cres(float))
